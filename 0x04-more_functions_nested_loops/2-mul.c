/**
 *mul - this function multiplies two integers
 *Return: returns the product of the two numbers
 *@a: the first integer given to the function to be multiplied
 *@b: the second integer given to the function to be multiplied
 */
int mul(int a, int b)
{
	int prod;

	prod = a * b;
	return (prod);
}
